;WITH CTE AS
(
    SELECT
        Id,
        SUM(Scheduled) OVER
        (
            PARTITION BY Designation,
                         [Year],
                         (([Month]-1)/3)
            ORDER BY [Year], [Month]
        ) AS Cum_Sched,

        SUM(Attended) OVER
        (
            PARTITION BY Designation,
                         [Year],
                         (([Month]-1)/3)
            ORDER BY [Year], [Month]
        ) AS Cum_Achieved
    FROM [NWKPI].[dbo].[PowerAndAC]
)
UPDATE P
SET
    Cumulative_Sched = CTE.Cum_Sched,
    Cumulative_Achieved = CTE.Cum_Achieved
FROM [NWKPI].[dbo].[PowerAndAC] P
JOIN CTE
ON P.Id = CTE.Id;
